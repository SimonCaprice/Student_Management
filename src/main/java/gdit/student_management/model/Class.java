package gdit.student_management.model;


public class Class {

  private String cId;
  private String cFaculty;
  private String cMajor;
  private String cClass;
  private Long cPeopleNumber;
  private String cHeadTeacher;
  private String cContactNumber;


  public String getCId() {
    return cId;
  }

  public void setCId(String cId) {
    this.cId = cId;
  }


  public String getCFaculty() {
    return cFaculty;
  }

  public void setCFaculty(String cFaculty) {
    this.cFaculty = cFaculty;
  }


  public String getCMajor() {
    return cMajor;
  }

  public void setCMajor(String cMajor) {
    this.cMajor = cMajor;
  }


  public String getCClass() {
    return cClass;
  }

  public void setCClass(String cClass) {
    this.cClass = cClass;
  }


  public Long getCPeopleNumber() {
    return cPeopleNumber;
  }

  public void setCPeopleNumber(Long cPeopleNumber) {
    this.cPeopleNumber = cPeopleNumber;
  }


  public String getCHeadTeacher() {
    return cHeadTeacher;
  }

  public void setCHeadTeacher(String cHeadTeacher) {
    this.cHeadTeacher = cHeadTeacher;
  }


  public String getCContactNumber() {
    return cContactNumber;
  }

  public void setCContactNumber(String cContactNumber) {
    this.cContactNumber = cContactNumber;
  }

}
