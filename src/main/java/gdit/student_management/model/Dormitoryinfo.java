package gdit.student_management.model;


public class Dormitoryinfo {

  private Long did;
  private String ddormbuilding;
  private Long sdormitoryid;
  private String dbedtotal;
  private String dbed;
  private String aname;


  public Long getDid() {
    return did;
  }

  public void setDid(Long did) {
    this.did = did;
  }


  public String getDdormbuilding() {
    return ddormbuilding;
  }

  public void setDdormbuilding(String ddormbuilding) {
    this.ddormbuilding = ddormbuilding;
  }


  public Long getSdormitoryid() {
    return sdormitoryid;
  }

  public void setSdormitoryid(Long sdormitoryid) {
    this.sdormitoryid = sdormitoryid;
  }


  public String getDbedtotal() {
    return dbedtotal;
  }

  public void setDbedtotal(String dbedtotal) {
    this.dbedtotal = dbedtotal;
  }


  public String getDbed() {
    return dbed;
  }

  public void setDbed(String dbed) {
    this.dbed = dbed;
  }


  public String getAname() {
    return aname;
  }

  public void setAname(String aname) {
    this.aname = aname;
  }

}
