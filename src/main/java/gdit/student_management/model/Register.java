package gdit.student_management.model;

public class Register {

  private Long oid;
  private String oname;
  private String owork;
  private String ophone;
  private String oreason;
  private java.util.Date createTime;


  public Long getOid() {
    return oid;
  }

  public void setOid(Long oid) {
    this.oid = oid;
  }


  public String getOname() {
    return oname;
  }

  public void setOname(String oname) {
    this.oname = oname;
  }


  public String getOwork() {
    return owork;
  }

  public void setOwork(String owork) {
    this.owork = owork;
  }


  public String getOphone() {
    return ophone;
  }

  public void setOphone(String ophone) {
    this.ophone = ophone;
  }


  public String getOreason() {
    return oreason;
  }

  public void setOreason(String oreason) {
    this.oreason = oreason;
  }


  public java.util.Date getCreateTime() {
    return createTime;
  }

  public void setCreateTime(java.util.Date createTime) {
    this.createTime = createTime;
  }

}
