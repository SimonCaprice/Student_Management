package gdit.student_management.model;

public class Student {

  private long sid;
  private String sname;
  private long sage;
  private String ssex;
  private String snativeplace;
  private String smajor;
  private String sclass;
  private String snative;


  public long getSid() {
    return sid;
  }

  public void setSid(long sid) {
    this.sid = sid;
  }


  public String getSname() {
    return sname;
  }

  public void setSname(String sname) {
    this.sname = sname;
  }


  public long getSage() {
    return sage;
  }

  public void setSage(long sage) {
    this.sage = sage;
  }


  public String getSsex() {
    return ssex;
  }

  public void setSsex(String ssex) {
    this.ssex = ssex;
  }


  public String getSnativeplace() {
    return snativeplace;
  }

  public void setSnativeplace(String snativeplace) {
    this.snativeplace = snativeplace;
  }


  public String getSmajor() {
    return smajor;
  }

  public void setSmajor(String smajor) {
    this.smajor = smajor;
  }


  public String getSclass() {
    return sclass;
  }

  public void setSclass(String sclass) {
    this.sclass = sclass;
  }


  public String getSnative() {
    return snative;
  }

  public void setSnative(String snative) {
    this.snative = snative;
  }

}
